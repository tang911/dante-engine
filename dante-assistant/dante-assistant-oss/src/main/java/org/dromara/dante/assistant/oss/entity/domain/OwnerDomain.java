package cn.herodotus.stirrup.assistant.oss.entity.domain;

import cn.herodotus.stirrup.assistant.oss.definition.domain.OssDomain;
import com.google.common.base.MoreObjects;

/**
 * <p>Description: 数据拥有者通用实体对象 </p>
 *
 * @author : gengwei.zheng
 * @date : 2024/7/21 22:52
 */
public class OwnerDomain implements OssDomain {

    private String displayName;

    private String id;

    public String getDisplayName() {
        return displayName;
    }

    public void setDisplayName(String displayName) {
        this.displayName = displayName;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    @Override
    public String toString() {
        return MoreObjects.toStringHelper(this)
                .add("displayName", displayName)
                .add("id", id)
                .toString();
    }
}
